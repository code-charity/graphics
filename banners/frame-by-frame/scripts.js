var banner = document.querySelector('.banner');


/*------------------------------------------------------------------------------
2.0 PARALLAX
------------------------------------------------------------------------------*/

window.addEventListener('mousemove', function(event) {
    var x = (event.clientY / (window.innerHeight / 100) - 50) * (16 / 100 * 2),
        y = (event.clientX / (window.innerWidth / 100) - 50) * (16 / 100 * 2);

    banner.style.transform = 'perspective(1000px) rotateY(' + y + 'deg) rotateX(' + -x + 'deg)';
});